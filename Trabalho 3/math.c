#include "math.h"

long double sum(long double a, long double b){
    return a+b;
}

long double subtract(long double a, long double b) {
    return a-b;
}

long double times(long double a, long double b) {
    return a*b;
}

long double divide(long double a, long double b) {
    return a/b;
}
